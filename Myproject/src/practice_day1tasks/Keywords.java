package practice_day1tasks;

public class Keywords {
		public static void main(String[] args) {

			final int x = 10;
			int y = 5;
			boolean result = true;
			byte b = 12;
			short s = 300;
			long l = 99999999L;
			float f = 3.14f;
			double d = 2.71828;
			char ch = 'A';
			String str = "Java";
			int count=0;
			while(count<1) {
				System.out.println("java keywords");
				count++;



				System.out.println("x: " + x);
				System.out.println("y: " + y);
				System.out.println("Result: " + result);
				System.out.println("Byte: " + b + ", Short: " + s);
				System.out.println("Long: " + l + ", Float: " + f + ", Double: " + d);
				System.out.println("Char: " + ch + ", String: " + str);
			}

		}
}
